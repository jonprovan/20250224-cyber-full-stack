package com.skillstorm;

public class OOPPillars {
	
	// THE FOUR PILLARS OF OBJECT-ORIENTED PROGRAMMING
	
	/*
	 * INHERITANCE
	 * - IS-A relationships, a Child IS-A Parent, etc.
	 * - avoid code duplication and reuse code
	 * - idempotence of actions, especially as it relates to DB operations
	 * 
	 * ABSTRACTION
	 * - hiding implementation details from other parts of the program, or from programmers/end users
	 * - abstract methods/classes, interfaces, Factory Pattern, etc.
	 * 
	 * POLYMORPHISM
	 * - covariance, i.e., a class can be thought of as different classes, based on inheritance structure
	 * - overriding, overloading
	 * - simplicity/ease of code use
	 * - "many forms"
	 * 
	 * ENCAPSULATION
	 * - hiding state/behavior of a class via access modifiers, getters/setters, data binding, etc.
	 * - wrapping up state/behavior in a class
	 * - in newer versions of Java, we can also use Modules
	 * 
	 */

}
